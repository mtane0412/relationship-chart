/**
 * 入力バリデーション用の定数定義
 *
 * このファイルは、アプリケーション全体で使用する入力値の制約を定義します。
 * セキュリティ対策として、過度に長い入力やサイズの大きいファイルを制限します。
 */

/**
 * 人物名の最大文字数
 *
 * ユーザーインターフェースの表示領域とデータサイズを考慮した制約です。
 */
export const MAX_PERSON_NAME_LENGTH = 50;

/**
 * 関係ラベルの最大文字数
 *
 * エッジラベルの表示領域を考慮した制約です。
 */
export const MAX_RELATIONSHIP_LABEL_LENGTH = 30;

/**
 * 画像ファイルの最大サイズ（バイト）
 *
 * 10MB = 10 * 1024 * 1024バイト
 * LocalStorageの容量制限（一般的に5-10MB）を考慮し、
 * リサイズ前の元画像のサイズ上限を設定します。
 */
export const MAX_IMAGE_FILE_SIZE_BYTES = 10 * 1024 * 1024;
